<?

	require_once($kapenta->installPath . 'modules/sync/models/notice.mod.php');

//-------------------------------------------------------------------------------------------------
//*	display an item from the sync queue
//-------------------------------------------------------------------------------------------------

	//---------------------------------------------------------------------------------------------
	//	auth and reference
	//---------------------------------------------------------------------------------------------
	if ('admin' != $user->role) { $page->do403(); }
	if ('' == $req->ref) { $page->do404(); }

	$model = new Sync_Notice($req->ref);
	if (false == $model->loaded) { $page->do404(); }

	//---------------------------------------------------------------------------------------------
	//	render the page
	//---------------------------------------------------------------------------------------------
	$page->load('modules/sync/actions/showqueueitem.page.php');
	$page->blockArgs['itemUID'] = $req->ref;
	$page->render();

?>
