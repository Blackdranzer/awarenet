	$shellCmd = 'ls '  . $installPath . 'gui/orphan/*.*';
	$result = shell_exec($shellCmd);
	$lines = explode("\n", $result);
	foreach($lines as $line) {
		$src = $line;

		$line = basename($line);
		$p1 = substr($line, 0, 1);
		$p2 = substr($line, 1, 1);
		$p3 = substr($line, 2, 1);

		$dir = 'data/images/' . $p1 . '/' . $p2 . '/' . $p3 . '/' . $line;

		echo $src . ' => ' . $dir . "<br/>\n";
	}

?>
