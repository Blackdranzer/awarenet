function central_listpeers($args) {
	global $db;
	global $user;
	global $theme;

	$html = '';			//%	return value [html]

	//----------------------------------------------------------------------------------------------
	//	check user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	query database
	//----------------------------------------------------------------------------------------------
	$range = $db->loadRange('central_peer', '*', '', 'name ASC');

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$block = $theme->loadBlock('modules/central/views/showpeer.block.php');

	foreach($range as $item) {
		$html .= $theme->replaceLabels();
	}

	return $html;
}


