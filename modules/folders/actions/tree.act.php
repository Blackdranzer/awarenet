<?

//--------------------------------------------------------------------------------------------------
//	list all galleries in root
//--------------------------------------------------------------------------------------------------

	if (authHas('folders', 'view', '') == false) { do403(); }			// check basic permissions

	//----------------------------------------------------------------------------------------------
	//	decide which users folders to show
	//----------------------------------------------------------------------------------------------

	$userUID = $user->data['UID'];
	if ($request['ref'] != '') {
		$userUID = raGetOwner($request['ref'], 'users');
		if ($userUID == false) { do404(); }
	}

	//----------------------------------------------------------------------------------------------
	//	render the page
	//----------------------------------------------------------------------------------------------

	$page->load($installPath . 'modules/folders/actions/tree.page.php');		
	$page->blockArgs['userUID'] = $userUID;								
	$page->render();													

?>
